package br.com.tamarozzi.model;

/**
 *
 * @author Panda
 */
public class Banco {
    
    private int id;
    private String nome;
    private String numero;

    public Banco() {
        
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }
}